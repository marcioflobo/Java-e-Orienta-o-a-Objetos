
public class Main {

    public static void main(String[] args) {
        
        double x = 30, y =51;
        
        // Soma:  "+"
        System.out.println("Operações envolvendo x & y");
        System.out.println("-----------------------------------");
        System.out.println("A Soma de x+y é : " + (x+y));
        
        System.out.println("-----------------------------------");
        System.out.println("A Subtração é : "+ (x-y));
        
        System.out.println("-----------------------------------");
        System.out.println("A Divisão é :"+(x/y));
        
        System.out.println("-----------------------------------");
        System.out.println("A Multiplicação é : "+(x*y));
        
        System.out.println("-----------------------------------");
        System.out.println("O Resto da divisão de x  é : "+ (x%2));
        System.out.println("O Resto da divisão de y  é : "+ (y%2));
        
    }
    
}
